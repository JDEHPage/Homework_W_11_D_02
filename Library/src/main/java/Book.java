public class Book {

    private String title;
    private String author;
    private String genere;


    public Book(String title, String author, String genere) {
        this.title = title;
        this.author = author;
        this.genere = genere;
    }

    public String getTitle() {
        return title;
    }

    public String getAuthor() {
        return author;
    }

    public String getGenere() {
        return genere;
    }





}
